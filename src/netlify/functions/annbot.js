export async function handler(event) {
  if (event.httpMethod !== "POST") {
    return { statusCode: 405, body: "Method not allowed" };
  }

  let body;
  try {
    body = JSON.parse(event.body || "{}");
  } catch {
    return { statusCode: 400, body: JSON.stringify({ error: "Invalid JSON" }) };
  }

  const { messages, sessionId, pageUrl } = body;
  if (!Array.isArray(messages) || messages.length === 0) {
    return { statusCode: 400, body: JSON.stringify({ error: "messages array required" }) };
  }

  const client = new Anthropic();

  try {
    const response = await client.messages.create({
      model: "claude-haiku-4-5-20251001",
      max_tokens: 1024,
      system: SYSTEM_PROMPT,
      messages
    });

    const reply = response.content[0].text;

    const supabase = getSupabase();
    if (supabase) {
      const turn = messages.filter(m => m.role === "user").length;
      supabase.from("annbot_messages").insert({
        session_id: sessionId ?? "unknown",
        turn,
        user_message: messages[messages.length - 1].content,
        bot_reply: reply,
        page_url: pageUrl ?? null
      }).then(() => {}).catch(() => {});
    }

    return {
      statusCode: 200,
      headers: { "content-type": "application/json" },
      body: JSON.stringify({ reply })
    };
  } catch (err) {
    console.error("Annbot error:", err);
    return {
      statusCode: 500,
      headers: { "content-type": "application/json" },
      body: JSON.stringify({ error: "Sorry, I'm having trouble connecting right now. Please try again in a moment." })
    };
  }
}
